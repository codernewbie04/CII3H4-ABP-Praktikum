    var nim = 1301204188;
    var isStudent = true;
    print("Nama : $name");
    print("NIM : $nim");
    print("Is Student ? $isStudent");

    // type annotation
    //Cara penggunaan : <type> <variable_name>;
    //Cara penggunaan : <type> <name> = <expression>;
